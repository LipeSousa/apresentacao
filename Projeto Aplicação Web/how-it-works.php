    <div class="how-card">
        <h1>Para que serve uma NFT?</h1><br>
        <p>Um NFT, ou Token Não Fungível, é uma forma de ativo digital que representa a propriedade ou autenticidade de
            um item digital ou físico.
            Ele é único e indivisível, ao contrário das criptomoedas como o Bitcoin ou Ethereum, que são fungíveis, ou
            seja,
            podem ser trocadas uma pela outra em partes iguais.</p><br>
        <p>Os NFTs têm uma variedade de usos, incluindo:</p><br>

        <p><i class='bx bxs-circle'></i> Colecionáveis Digitais: O NFT pode representar obras de arte digitais, vídeos,
            músicas, memes, tweets e outros tipos de conteúdo digital.</p><br>
        <p><i class='bx bxs-circle'></i> Gaming e Virtual Worlds: NFTs podem ser utilizados em jogos online e mundos
            virtuais para representar itens exclusivos, como skins de personagens, armas, terrenos virtuais, entre
            outros.</p><br>
        <p><i class='bx bxs-circle'></i> Mercado de Arte: Artistas podem vender obras de arte digitais como NFTs,
            permitindo-lhes manter o controle da autenticidade e rastreabilidade de suas obras.</p><br>
        <p><i class='bx bxs-circle'></i> Tokenização de Ativos Físicos: NFTs também podem representar ativos do mundo
            real, como imóveis, carros de luxo, relíquias históricas e outros objetos colecionáveis.</p>

    </div>
    <div class="how-card">
        <h1>Como funciona os NFTs?</h1><br>
        <p>Os NFTs funcionam na blockchain, geralmente baseados em plataformas como Ethereum, Binance Smart Chain ou
            outras blockchains específicas para NFTs. Aqui está um breve resumo de como funcionam:</p><br>

        <p><i class='bx bxs-circle'></i> Criação: Um criador decide tokenizar um item digital ou físico, transformando-o
            em um NFT. Isso geralmente é feito através de uma plataforma de mercado NFT.</p><br>
        <p><i class='bx bxs-circle'></i> Mintagem: O NFT é "minado" ou criado na blockchain por meio de um processo
            chamado "mintagem". Isso envolve a gravação das informações do NFT na blockchain, como sua propriedade,
            metadados e outras informações relevantes.</p><br>
        <p><i class='bx bxs-circle'></i> Transações: Os NFTs podem ser comprados, vendidos e trocados entre usuários em
            mercados NFT. Cada transação é registrada na blockchain, garantindo a autenticidade e a proveniência do NFT.
        </p><br>
        <p style="padding-bottom: 25px;"><i class='bx bxs-circle'></i> Propriedade e autenticidade: A propriedade e a autenticidade de um NFT são
            garantidas pela blockchain. O registro descentralizado da blockchain permite que qualquer pessoa verifique a
            propriedade e a história de um NFT.

        </p>

    </div>
    <div class="how-card">
        <h1>Como lucrar com NFTs?</h1><br>
        <p>Há várias maneiras de lucrar com NFTs:</p><br>

        <p><i class='bx bxs-circle'></i> Compra e Venda: Assim como com qualquer ativo, você pode comprar NFTs a um
            preço mais baixo e vendê-los a um preço mais alto. Isso requer pesquisa de mercado e identificação de NFTs
            que tenham potencial de valorização.</p><br>
        <p><i class='bx bxs-circle'></i> Criação e Venda de Arte: Se você é um artista digital, pode criar suas próprias
            obras de arte e vendê-las como NFTs em plataformas de mercado NFT. Se suas obras se tornarem populares, você
            pode lucrar com suas vendas.</p><br>
        <p><i class='bx bxs-circle'></i> Investimento em Coleções: Você pode investir em coleções de NFTs de artistas ou
            projetos que acredita terem potencial de valorização a longo prazo.</p><br>
        <p><i class='bx bxs-circle'></i> Participação em Mercados Secundários: Alguns NFTs geram receita recorrente para
            seus proprietários, como royalties sobre vendas futuras. Você pode comprar esses NFTs e lucrar com as
            receitas geradas.</p><br>

        <p>No entanto, é importante notar que o mercado de NFTs pode ser volátil e especulativo, e o lucro não é
            garantido. É essencial fazer uma pesquisa cuidadosa e entender os riscos antes de investir em NFTs.</p>
